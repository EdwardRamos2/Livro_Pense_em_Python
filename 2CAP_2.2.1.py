# Qual e o volume de uma esfera com raio 5?


r = 5           #Valor do raio (5)
pi = 3.14       #Valor de pi (3.14)

r = 5 ** 3      #Valor do raio elevado a 3:  (5 ** 3) => 125

v = 4 * pi * r  #Calculo v = 1570

volume = v / 3       #1570 / 3 = 523.3 

print (volume)      #523.3 Litros

